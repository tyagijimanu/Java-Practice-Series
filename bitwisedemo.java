public class bitwisedemo {
  public static void main(String[] args) {
    int x = 1;
    int y = 2;
  
    int z=x&y;
    int a=x|y;
    int b=x^y;
    int c=x<<1;
    int d=x>>1;
    int e=x<<2;
    
    System.out.println(z);
    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
    
  }  
}
